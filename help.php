    <center><b>General</b></center>
    <br>
    The site in general works on a tiered system. What this means is that you need a certain access level to access
    certain pages. Minglers/Tour Guides will all be able to access coverage and form pages, but some pages will only
    be able to be accessed by HDC and SLC. If you are asked for a username/password for a page, and don't know it,
    you probably shouldn't be trying to get to that page.
    <br>
    <br>
    All visible links will be in the navigation at the top of the page, so you can navigate the site from there. Please
    let Matt know if any of the links are broken!

    <br>
    <br>
    <center><b>The Check-In Page</b></center>
    <br>
    The Check-In Page should only be accessed from the Visitor Center iPad. It's only use is to check in for tours, cover
    a tour, or shadow a tour, therefore it is unnecessary to access outside of the VC.
    <br>
    <br>
    When you come into the VC, the check-in system will display your name in the drop-down 30 minutes (15 for minglers)
    before your shift. If you do not see your name within this window, simply REFRESH the page.
    Simply choose your name from the drop down menu and click check-in. It will then inform you that you have checked in
    for your tour.
    <br><br>
    With the new coverage system, when you come in to cover a tour, your name will now be in the drop-down with a (C)
    next to it. However, if this is a last minute coverage, you can still select the "Coverage/Additional Tour" checkbox and select
    your name from the new drop-down (after having selected who you are covering for from the first selection). This
    will also work for giving an additional tour if you do not select anyone from the first drop-down menu.
    <br><br>
    Shadowing a tour for new hires/minglers is easy! Just click the Shadowing checkbox and select your name from the
    list that will appear, then click Check-in and you're good to go.
    <br><br>
    <center><b>Requesting Coverage</b></center>
    <br>
    Simply follow the link on the navigation bar at the top of the page named "Request Coverage." This will take you to
    the form to fill out when looking for somebody to cover your tour. Fill out the appropriate fields and click submit.
    This will then email anybody who has stated they are available for the tour time you have selected. Once you fill this
    form out, your coverage will be posted on the Coverage Board, explained in the next sub-heading.
    <br><br>
    <center><b>Coverage Board</b></center>
    <br>
    The link for the Coverage Board exists on the same navigation bar at the top of the page. Choosing this option will
    bring up a list of tours that need covering. You can browse through them and see the date, type of tour, and what
    time the tour is at. If you choose to accept the tour coverage, simply click "Accept Shift" and input your name on
    next page. Clicking submit will make you responsible for that tour shift and if you miss the tour, you will receive
    a strike.

